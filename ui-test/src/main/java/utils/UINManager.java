package utils;

import static io.mosip.testrig.apirig.utils.AdminTestUtil.autoGeneratedIDValueCache;

import java.util.concurrent.BlockingQueue;
import java.util.concurrent.LinkedBlockingQueue;
import java.util.concurrent.TimeUnit;

import models.Uin;

public class UINManager {

	private static final BlockingQueue<Uin> availableUINs = new LinkedBlockingQueue<>();

	static {
		String uins = EsignetConfigManager.getproperty("uin");
		String plugin = EsignetConfigManager.getproperty("pluginToExecute");

		if (uins != null && !uins.isEmpty()) {
			String[] uinList = uins.split(",");

			for (String uin : uinList) {
				availableUINs.add(new Uin(uin.trim()));
			}

		} else {
			if ("mock".equalsIgnoreCase(plugin)) {
				loadMockUIN();

			} else if ("mosipid".equalsIgnoreCase(plugin)) {
				loadMosipidUINs();

			} else {
				throw new IllegalStateException(
						"Invalid pluginToExecute value. Expected: mock or mosipid, Found: " + plugin);
			}

			if (availableUINs.isEmpty()) {
				throw new IllegalStateException("No UINs available from config or TC execution");
			}
		}
	}

	// Cache keys mirror AdminTestUtil#getAutogenIdKeyName, which strips a testcase name down to
	// everything after its first underscore before appending the field name - so the module
	// prefix ("ESignetUI_"/"EsignetUI_"/"ESignet_") in the yml testcase name is never part of
	// the stored key.
	private static void loadMockUIN() {
		String uin = autoGeneratedIDValueCache.get("AddIdentity_Valid_Parameters_smoke_Pos_UIN");
		if (uin != null && !uin.isEmpty()) {
			availableUINs.add(new Uin(uin));
		}
	}

	private static void loadMosipidUINs() {
		// The two "_Pos" testcases AddIdentity.yml actually defines today.
		String[] cacheKeys = { "AddIdentity_withValidParameters_smoke_Pos_UIN",
				"AddIdentity_Vid_Generation_smoke_Pos_UIN" };

		for (String cacheKey : cacheKeys) {
			String uin = autoGeneratedIDValueCache.get(cacheKey);
			if (uin != null && !uin.isEmpty()) {
				availableUINs.add(new Uin(uin));
			}
		}
	}

	public static Uin acquireUIN() throws InterruptedException {
		return availableUINs.take();
	}

	public static void releaseUIN(Uin uin) {
		availableUINs.offer(uin);
	}

	public static Uin acquireUINWithTimeout(long timeoutSeconds) throws InterruptedException {
		return availableUINs.poll(timeoutSeconds, TimeUnit.SECONDS);
	}
}